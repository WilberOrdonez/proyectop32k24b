    public void consultarprocesos(){
    String idPago = txtIDPago.getText();
    String codigoMaestro = txtCodigoMaestro.getText();
    boolean encontrado = false;

    DefaultTableModel modelo = (DefaultTableModel) tablaProcesoPagoMaestro.getModel();
    int rowCount = modelo.getRowCount();

    for (int i = 0; i < rowCount; i++) {
        String idPagoTabla = (String) modelo.getValueAt(i, 0);
        String codigoMaestroTabla = (String) modelo.getValueAt(i, 1);

        if (idPago.equals(idPagoTabla) && codigoMaestro.equals(codigoMaestroTabla)) {
            encontrado = true;
            break;
        }
    }

    if (encontrado) {
        JOptionPane.showMessageDialog(this, "Datos verificados correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
    } else {
        JOptionPane.showMessageDialog(this, "No se encontraron los datos ingresados.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }
    
