    public void llenadoDeTablas() {
         DefaultTableModel modelo = (DefaultTableModel) tablaProcesoPagoMaestro.getModel();
     modelo.setRowCount(0);
     
     ProcesosTiposPagosMaestrosDAO procesotipopagomaestroDAO = new ProcesosTiposPagosMaestrosDAO();
     List<ProcesosTiposPagosMaestros> tipopagosmaestros = procesotipopagomaestroDAO.select();
     
     for (ProcesosTiposPagosMaestros tipopagomaestro: tipopagosmaestros){
         Object[] fila = new Object[]{
           tipopagomaestro.getProcesoIDPago(),
           tipopagomaestro.getProcesoCodigoMaestro(),
         };
         modelo.addRow(fila);
    }
     
    }
    
